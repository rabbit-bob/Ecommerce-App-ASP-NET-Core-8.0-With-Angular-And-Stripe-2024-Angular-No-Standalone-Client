// Represents the structure of an address associated with a user
export interface IAddress {
    firstName: string; // First name of the user
    lastName: string;  // Last name of the user
    street: string;    // Street address of the user
    city: string;      // City of the user's address
    state: string;     // State of the user's address
    zipCode: string;   // ZIP code of the user's address
  }